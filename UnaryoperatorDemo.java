package day_4;

public class UnaryoperatorDemo {

    public static void main(String[] args) {
        int  x = 20;
        int y;

        
         y = ++x;
        System.out.println(+y);
    
         y = --x;
        System.out.println(+y);
        
        y = x++;
        System.out.println(+y);
        
       /* y = x;
        System.out.println(+y); */
        
        y = x--;
        System.out.println(+y);
    
        
    
         
    
    }

}
