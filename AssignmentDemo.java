
package day4;


public class AssignmentDemo {
    public static void main(String[] args){
    int x = 10;
    int y = 5;
    
    x+=y;
    System.out.println(+x);
    
    x-=y;
    System.out.println(+x);
    
    x*=y;
    System.out.println(+x);
    
    x/=y;
    System.out.println(+x);
    
    x%=y;
    System.out.println(+x);
    
    }
    
}
