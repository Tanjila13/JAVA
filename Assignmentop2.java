
package pkg611basicday1;


public class Assignmentop2 {
    public static void main(String[] args) {
        
       int a =10;
       int b = 20;
       
       a+=b; //a = a+b
       System.out.println("Result: "+a);
       
       a-=b; //a = a-b
       System.out.println("Result: "+a);
       
       a*=b; //a = a*b
       System.out.println("Result: "+a);
       
       a/=b; //a = a/b
       System.out.println("Result: "+a);
       
       a%=b; //a = a%b
       System.out.println("Result: "+a);
       
       
        
    }
    
}
